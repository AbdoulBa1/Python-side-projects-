width = int(input("Enter the width for the rectangle: "))
length = int(input("Enter the length for the rectangle: "))
Area = width * length
Perimeter = 2*(width+length)
Area_txt = f"Area of the rectangle: {Area}"
Perimeter_txt = f"Perimeter of the rectangle: {Perimeter}"
print(Area_txt)
print(Perimeter_txt)